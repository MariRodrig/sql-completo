SMALLINT: maior que o tinyint (-32.768 a 32.767)
INT: numeros inteiros
BIGINT: inteiro grande
DECIMAL: decimal preciso(p,s)
FLOAT: decimal aproximado

DATAS
DATE: data 
TIME: hora 
DATETIME: data + hora

BOOLEANOS
BIT: por padrao e inicializado como nulo, podendo receber 0 ou 1 */

